-- 동명 동물 수 찾기
-- 처음에 1처럼 풀었는데 ORDER BY 안써서 틀린지 모르고 지피티로 2로 고침;

-- 1
SELECT NAME, COUNT(NAME) AS COUNT
FROM ANIMAL_INS
GROUP BY NAME
HAVING NAME IS NOT NULL
AND COUNT(NAME) > 1
ORDER BY NAME;

-- 2
SELECT NAME, COUNT(*) AS COUNT
FROM ANIMAL_INS
WHERE NAME IS NOT NULL
GROUP BY NAME
HAVING COUNT(*) > 1
ORDER BY NAME;