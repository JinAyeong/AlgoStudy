-- 연도별 대장균 크기의 편차 구하기

SELECT YEAR(E1.DIFFERENTIATION_DATE) AS YEAR, (E2.SIZE - E1.SIZE_OF_COLONY) AS YEAR_DEV, ID
FROM ECOLI_DATA E1
LEFT JOIN 
(SELECT MAX(SIZE_OF_COLONY) AS SIZE, YEAR(DIFFERENTIATION_DATE) AS YEAR
FROM ECOLI_DATA
 GROUP BY YEAR(DIFFERENTIATION_DATE)
) AS E2
ON YEAR(E1.DIFFERENTIATION_DATE) = E2.YEAR
ORDER BY YEAR ASC, YEAR_DEV ASC